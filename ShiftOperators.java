public class ShiftOperators {
    public static void main(String[] args) {
        //left and right shift operators
        int a=10;
        int leftshift=a<<2;
        System.out.println("left shift of "+a+" is "+leftshift);
        int rightshift=a>>1;
        System.out.println("right shift of "+a+" is "+rightshift);

    }
    
}
